namespace Portoa.Web.Rest {
	/// <summary>
	/// Values representing the order in which to sort a collection of values
	/// </summary>
	public enum SortOrder {
		/// <summary>
		/// Sort from least to most
		/// </summary>
		Ascending,
		/// <summary>
		/// Sort from most to least
		/// </summary>
		Descending
	}
}